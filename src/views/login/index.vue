<template>
  <div class="login-container">
    <el-form ref="loginForm" :model="loginForm" :rules="loginRules"
             class="login-form" auto-complete="on" label-position="left">
      <!--ref,model绑定输入数据 :rules验证规则 auto-complete自动键入 label-position form的label标签对齐位置-->
      <h3 class="title">登录页面</h3>
      <el-form-item prop="username">
        <span class="svg-container">
          <svg-icon icon-class="user" />
        </span>
        <!--placeholder  输入框占位文本-->
        <el-input v-model="loginForm.username" name="username" type="text"
                  auto-complete="on" placeholder="用户名" />
      </el-form-item>
      <el-form-item prop="password">
        <span class="svg-container">
          <svg-icon icon-class="password" />
        </span>
        <!--v-bind:type  对type属性动态改变
           keyup.enter.native   重写回车敲击事件,仅限当前input-->
        <el-input
          v-bind:type="pwdType"
          v-model="loginForm.password"
          name="password"
          auto-complete="on"
          placeholder="密码"
          @keyup.enter.native="handleLogin" />
        <span class="show-pwd" @click="showPwd">
          <svg-icon icon-class="eye" />
        </span>
      </el-form-item>
      <el-form-item>
        <el-button :loading="loading" type="primary" style="width:100%;" @click.native.prevent="handleLogin">
          登  录
        </el-button>
      </el-form-item>

    </el-form>
  </div>
</template>

<script>
import { isvalidUsername } from '@/utils/validate'

export default {
  name: 'Login',
  data() {
    //验证要求
    const validateUsername = (rule, value, callback) => {
      if (value==='') {
        callback(new Error('用户名不能为空'))
      } else {
        callback()
      }
    };
    const validatePass = (rule, value, callback) => {
      if (value==='') {
        callback(new Error('密码不能为空'))
      } else {
        callback()
      }
    };
    return {
      loginForm: {
        username: '',
        password: ''
      },
      loginRules: {
        username: [{ required: true, trigger: 'blur', validator: validateUsername }],
        password: [{ required: true, trigger: 'blur', validator: validatePass }]
      },
      loading: false,
      pwdType: 'password',
      redirect: undefined
    }
  },
  watch: {
    $route: {
      handler: function(route) {
        this.redirect = route.query && route.query.redirect
      },
      immediate: true
    }
  },
  methods: {
    showPwd() {
      if (this.pwdType === 'password') {
        this.pwdType = ''
      } else {
        this.pwdType = 'password'
      }
    },
    handleLogin() {
      this.$refs.loginForm.validate(valid => {

        if (valid) {
          this.loading = true
          //异步传值
          this.$store.dispatch('Login', this.loginForm).then(() => {
            this.loading = false
            this.$router.push({ path:  '/' })  //成功登陆后重定向到首页
          }).catch(() => {
            this.loading = false
          })
        } else {
          console.log('error submit!!')
          return false
        }
      })
    }
  }
}
</script>

